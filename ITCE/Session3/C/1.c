#include <stdio.h> //นำเข้าฟังก์ชั่นทั่วไปจากLibraly

int number; //กำหนดตัวแปร Number

int main(){ //ฟังก์ชั่นพื้นฐาน
    printf("input number"); //ส่งออกข้อความขอเลขจากผู้ใช้
    scanf("%d",&number);//รับค่าตัวเลข
    for(int i=1;i<=12;i++){//วนซ้ำ เริ่มจาก1 ในระหว่างที่น้อยกว่าหรือเท่ากับ12 เพิ่มขึ้นที่ละ1
        printf("%d x %d = %d\n",number,i,(number*i));//นำออกค่าเลขที่ได้รับ คูณกับรอบที่นับอยู่ และผลลัพธ์
    }
}