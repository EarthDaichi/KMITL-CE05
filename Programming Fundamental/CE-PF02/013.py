A = 100; B = 3; C = 12.25; D = 94.62
print('---A = 100, B = 3, C = 12.25, D = 94.62 ---')
ans = A+D; print("A+B = ", ans)
ans = A//B; print("A//B = ", ans)
ans = A%B; print("A%B = ", ans)
ans = C**B; print("A^B = ", ans)
ans = D/C; print("D/C = ", ans)
ans = A*B*C*D; print("A*B*C*D = ", ans)
ans = D%B; print("D%B = ", ans)